#include <iostream>
using namespace std;
int main()
{
	int a, b;
	cout<<"\nEnter the value of A and B : ";
	cin>>a>>b;
	// Sum (+)
	cout<<a<<" + "<<b<<" = "<<a+b<<endl;
	// Subtraction (-)
	cout<<a<<" - "<<b<<" = "<<a-b<<endl;
	// Product (*)
	cout<<a<<" * "<<b<<" = "<<a*b<<endl;
	// Division/Quotient (/)
	cout<<a<<" / "<<b<<" = "<<a/b<<endl;
	return 0;
}

